export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations.en

export function getTranslation(lang: Language, key: TranslationKey): string {
  return translations[lang]?.[key] || translations.en[key] || key
}

export function translateCategory(lang: Language, category: string): string {
  return translations[lang]?.[category as TranslationKey] || category
}

export function translateType(lang: Language, type: string): string {
  return translations[lang]?.[type as TranslationKey] || type
}

